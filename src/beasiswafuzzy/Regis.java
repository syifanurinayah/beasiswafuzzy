    private void bregisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bregisActionPerformed
        String t1 = txt_id.getText();
        String t2 = txt_namleng.getText();
        String t3 = txt_nid.getText();
        String t4 = txt_user.getText();
        String t5 = txt_pass.getText();
        try {
            if (edit == true) {
                stm.executeUpdate("update user set id='" + t1 + "',namleng='" + t2 + "',nid='" + t3 + "',username='" + t4 + "',password='" + t5 +  "'where id='" + t1 + "'");
            } else {
                stm.executeUpdate("INSERT into user VALUES('" + t1 + "','" + t2 + "','" + t3 +  "','" + t4 +  "','" + t5 +  "')");
            }
            tbl_data.setModel(new DefaultTableModel(dataTable, header));
            tampil();
            aktif(false);
            setTombol(true);
            kosong();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
             new login().setVisible(true);
             this.dispose();
        }
    }//GEN-LAST:event_bregisActionPerformed

