        private void autonomor(){
        String sql="select max(id)from user";
        try{
            stm=koneksi.createStatement();
            RsBrg=stm.executeQuery(sql);
            while(RsBrg.next()){
                int a=RsBrg.getInt(1);
                txt_id.setText(""+Integer.toString(a+1));    
            }
        }catch(Exception e){
            System.out.println(""+e.getMessage());
        }
    }
      
    void tampil(){
        tabModel=new DefaultTableModel(null,header);
        try{
            stm=koneksi.createStatement();
            RsBrg=stm.executeQuery("select*from user");
            while(RsBrg.next()){
            String[]data={
                
        RsBrg.getString("id"),RsBrg.getString("namleng"),RsBrg.getString("nid"),RsBrg.getString("username"),RsBrg.getString("password")};     
        tabModel.addRow(data);
            }
            tbl_data.setModel(tabModel);
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,e+"Data Gagal Tampil.");
                System.out.println("err:"+e);
        }}

